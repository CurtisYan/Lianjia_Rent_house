// 地块到区域的反向映射
export const SUBDISTRICT_TO_DISTRICT = {}
Object.entries(DISTRICT_SUBDISTRICT_MAP).forEach(([district, subdistricts]) => {
  subdistricts.forEach(subdistrict => {
    SUBDISTRICT_TO_DISTRICT[subdistrict] = district
  })
})

// 获取指定区域的地块列表
export function getSubdistrictsForDistrict(district) {
  return DISTRICT_SUBDISTRICT_MAP[district] || []
}

// 根据地块名称获取所属区域
export function getDistrictForSubdistrict(subdistrict) {
  return SUBDISTRICT_TO_DISTRICT[subdistrict] || '未知'
}

// 检查地块是否属于指定区域
export function isSubdistrictInDistrict(subdistrict, district) {
  const subdistrictList = DISTRICT_SUBDISTRICT_MAP[district] || []
  return subdistrictList.includes(subdistrict)
} 